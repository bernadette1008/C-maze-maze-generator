void CursorView(char show) //커서를 없애는 함수
{   
    //구조체 선언
    CONSOLE_CURSOR_INFO ConsoleCursor; //콘솔의 커서를 다루는 구조체 선언

    //ConsoleCursor 구조체의 bVisible, dwSize 값 정의
    ConsoleCursor.bVisible = show;// 0 ~ 1 == hidden ~ show
    ConsoleCursor.dwSize = 1; // 1과 100사이의 값을 가져 커서의 크기 조절(필요는 없으나 초기화를 위해 입력됌)

    //GetStdHandle(STD_OUTPUT_HANDLE)의 설정을 ConsoleCursor로 바꾸기(커서 숨기고, 사이즈 1로 변경)
    //SetConsoleCursorInfo = 콘솔커서설정을 의미
    //CONSOLE_CURSOR_INFO라는 구조체를 통해 미리 선언해둔
    //설정을 표준콘솔출력에 적용하여 커서의 형태를 변경한다.
    SetConsoleCursorInfo(GetStdHandle(STD_OUTPUT_HANDLE),&ConsoleCursor); 
}

void GotoXY(int x,int y) //콘솔 위에 커서 위치를 바꾸어 원하는 곳에 미로를 출력하기 위한 함수
{   
    //구조체 선언
    COORD Pos; //좌표를 정의하는 구조체 선언

    //Pos 구조체의 x, y 값 정의
    Pos.X = x;
    Pos.Y = y;

    //입력된 위치에서 화면을 출력
    //GetStdHandle(STD_OUTPUT_HANDLE) = 표준콘솔출력의 핸들을 의미
    //SetConsoleCursorPosition = 콘솔커서위치라는 의미답게
    //커서의 위치를 조작하는 함수이다.

    //즉 COORD Pos를 통해 선언한 x, y 값을 표준콘솔출력에 적용함으로써 커서의 위치를 변경한다.
    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE),Pos);
}
 
int GetKey() //키를 받아들이는 함수
{
    //_kbhit = 키보드 입력 시 1을 반환
    if (_kbhit() != 0)
    {
        return _getch(); //입력한 키 반환
    }
 
    return 0; //그렇지 않을 경우 0반환
}
 
void print_mazeGame(char maze[][MAX_SIZE],int row) //미로 frame(틀)을 그려주는 함수
{

    for (int i = 0; i < row; i++)
    {
        //배열 값에 따라 출려되는 블럭 형태 지정
        GotoXY(XPOS-18,YPOS + i);
        for (int j = 0; j < MAX_SIZE; j++)
        {
            if (maze[i][j] == '1'){
                SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), WHITE); // 텍스트 색상 설정(WHITE)
                printf("■");    //벽
            }
            else if (maze[i][j] == 'y'){
                SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), YELLOW); // 텍스트 색상 설정(YELLOW)
                printf("★");   //클리어 지점
            }
            else if (maze[i][j] == '0'){
                SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), WHITE); // 텍스트 색상 설정(WHITE)
                printf("□");    //길

            }
            else{
                SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), BLUE); // 텍스트 색상 설정(BLUE)
                printf("●");    //플레이어
            }
        }
        puts("");
    }
}
 
int is_block(char maze[][MAX_SIZE], int i, int j)
{
    if (maze[i][j] == '1' || maze[i][j] == 'y') //미로가 벽일 경우, 종점일 경우
        return 1;
    else 
        return 0;
}
 
int is_finish(char maze[][MAX_SIZE],int i,int j)
{
 
    if (maze[i][j] == 'y') //종점일 경우
        return 1;
    else
        return 0;
}
 
